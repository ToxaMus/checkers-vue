/**
 * Перечисление цветов игроков (сторон)
 * Используется для определения принадлежности фигур и очерёдности хода
 */
export enum Color {
  WHITE = "white",   // Белые шашки (обычно ходят первыми)
  BLACK = "black"    // Чёрные шашки
}

/**
 * Перечисление цветов границы клетки
 * Используется для визуального выделения клеток при навигации с клавиатуры
 */
export enum BorderColor {
  DEFAULT = "transparent",  // Стандартная граница (прозрачная, невидимая)
  ACTIVE = "#ff9800"        // Активная граница (оранжевая) - для выделения текущей клетки
}

/**
 * Перечисление цветов фона клетки
 * Определяет визуальное состояние клетки в различных игровых ситуациях
 */
export enum BackgroundColor {
  LIGHT = "#FFFFF0",        // Светлый фон (белые клетки) - неигровые клетки
  DARK = "#D3D3D3",         // Тёмный фон (чёрные/серые клетки) - игровые клетки
  SELECTED = "#4CAF50",     // Выделенная клетка (зелёная) - клетка с выбранной фигурой
  MOVE = "#2196F3",         // Возможный ход (синяя) - подсветка доступных для хода клеток
  ENEMY = "#f44336",        // Враг (красная) - подсветка клеток с вражескими фигурами (для отладки)
  POSSIBLE_EATING = "#FF8C00" // Возможное взятие (оранжевая) - подсветка клеток для съедения врага
}
